# The following code creates a list 'first_list' containing 4 integers
first_list = [1, 2, 3, 4]

# The clear() function removes all elements from 'first_list'
first_list.clear()

# The resulting empty list is printed to the console
print(first_list)

# The following code creates a list 'second_list' containing 4 strings
second_list = ['a', 'b', 'c', 'd']

# The pop() function removes and returns the last element in 'second_list', which is 'd'
d = second_list.pop()

# The value of 'd' is printed to the console
print(d)

# The resulting list with 'd' removed is printed to the console
print(second_list)
